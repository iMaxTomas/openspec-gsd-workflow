#!/usr/bin/env node
/**
 * GSD Pause Work - Create checkpoint and handoff
 * Saves current state for later resumption
 */

import { writeFileSync, mkdirSync, existsSync, readFileSync } from 'fs';
import { join } from 'path';
import { spawnSync } from 'child_process';

class GsdPauseWork {
  constructor(changeId, projectRoot = process.cwd()) {
    this.changeId = changeId;
    this.projectRoot = projectRoot;
    this.timestamp = new Date().toISOString();
    this.sessionId = this.generateSessionId();
  }

  generateSessionId() {
    return `${this.changeId}-session-${Date.now()}`;
  }

  async execute() {
    console.log(`⏸️  GSD Pause Work: ${this.changeId}\n`);

    try {
      // 1. Collect current state
      const state = await this.collectState();
      
      // 2. Create checkpoint
      await this.createCheckpoint(state);
      
      // 3. Create handoff document
      await this.createHandoff(state);
      
      // 4. Create GSD HANDOFF.json
      await this.createHandoffJson(state);
      
      // 5. Commit changes if git repo
      await this.commitChanges();

      console.log('\n✅ Pause work completed successfully!\n');
      console.log(`Session ID: ${this.sessionId}`);
      console.log(`Resume with: /gsd-resume-work`);
      console.log(`Or manually: cat .continue-here.md\n`);

      return {
        success: true,
        sessionId: this.sessionId,
        timestamp: this.timestamp
      };
    } catch (error) {
      console.error('\n❌ Failed to pause work:', error.message);
      return {
        success: false,
        error: error.message
      };
    }
  }

  async collectState() {
    console.log('📊 Collecting current state...');

    // Get git status
    const gitStatus = spawnSync('git', ['status', '--porcelain'], {
      encoding: 'utf8',
      cwd: this.projectRoot
    });

    // Get current branch
    const gitBranch = spawnSync('git', ['branch', '--show-current'], {
      encoding: 'utf8',
      cwd: this.projectRoot
    });

    // Get last commit
    const lastCommit = spawnSync('git', ['log', '-1', '--oneline'], {
      encoding: 'utf8',
      cwd: this.projectRoot
    });

    // Read context monitor state if exists
    let contextState = null;
    const contextFile = join(this.projectRoot, '.opencode', 'passive', `context-monitor-${this.changeId}.json`);
    if (existsSync(contextFile)) {
      try {
        contextState = JSON.parse(readFileSync(contextFile, 'utf8'));
      } catch {}
    }

    return {
      timestamp: this.timestamp,
      sessionId: this.sessionId,
      changeId: this.changeId,
      branch: gitBranch.stdout?.trim() || 'unknown',
      gitStatus: gitStatus.stdout?.trim() || '',
      lastCommit: lastCommit.stdout?.trim() || '',
      contextState,
      workingDirectory: this.projectRoot
    };
  }

  async createCheckpoint(state) {
    console.log('📝 Creating checkpoint...');

    const checkpointDir = join(this.projectRoot, '.gsd', 'phases', this.changeId);
    mkdirSync(checkpointDir, { recursive: true });

    const checkpointContent = this.renderCheckpoint(state);
    const checkpointPath = join(checkpointDir, `checkpoint-${this.sessionId}.md`);
    
    writeFileSync(checkpointPath, checkpointContent);
    
    // Also update main checkpoint
    const mainCheckpointPath = join(this.projectRoot, '.opencode', 'passive', 'checkpoint.md');
    writeFileSync(mainCheckpointPath, checkpointContent);

    console.log(`   Checkpoint: ${checkpointPath}`);
  }

  renderCheckpoint(state) {
    return `# checkpoint

## Session Info
- timestamp: ${state.timestamp}
- session_id: ${state.sessionId}
- change_id: ${state.changeId}
- branch: ${state.branch}

## Git Status
\`\`\`
${state.gitStatus || 'Clean'}
\`\`\`

## Last Commit
${state.lastCommit || 'None'}

## Context State
${state.contextState ? `- Messages: ${state.contextState.messageCount}/15` : '- Not available'}

## How to Resume
1. Run: \`/gsd-resume-work\`
2. Or read: \`.continue-here.md\`

---
Generated by GSD Pause Work
`;
  }

  async createHandoff(state) {
    console.log('📋 Creating handoff document...');

    const handoffContent = this.renderHandoff(state);
    const handoffPath = join(this.projectRoot, '.continue-here.md');
    
    writeFileSync(handoffPath, handoffContent);

    // Also save in .gsd directory
    const gsdHandoffPath = join(this.projectRoot, '.gsd', 'phases', this.changeId, `.continue-here-${this.sessionId}.md`);
    writeFileSync(gsdHandoffPath, handoffContent);

    console.log(`   Handoff: ${handoffPath}`);
  }

  renderHandoff(state) {
    return `# Continue Here

## Session
- **Change**: ${state.changeId}
- **Session**: ${state.sessionId}
- **Paused At**: ${state.timestamp}
- **Branch**: ${state.branch}

## Current State
${state.gitStatus ? '### Modified Files\n' + state.gitStatus.split('\n').map(s => '- ' + s).join('\n') : '### Status\nClean working directory'}

## Context
${state.contextState ? `- Messages in session: ${state.contextState.messageCount}` : '- Context monitoring not active'}

## Resume
\`\`\`bash
# Resume this session
/gsd-resume-work

# Or manually continue from here
cat .gsd/phases/${state.changeId}/checkpoint-${state.sessionId}.md
\`\`\`

---
*This file was automatically generated by /gsd-pause-work*
`;
  }

  async createHandoffJson(state) {
    console.log('📦 Creating HANDOFF.json...');

    const handoffData = {
      timestamp: state.timestamp,
      session_id: state.sessionId,
      change_id: state.changeId,
      branch: state.branch,
      git_status: state.gitStatus,
      last_commit: state.lastCommit,
      context_state: state.contextState,
      working_directory: state.workingDirectory
    };

    const handoffPath = join(this.projectRoot, '.gsd', 'phases', this.changeId, `HANDOFF-${this.sessionId}.json`);
    writeFileSync(handoffPath, JSON.stringify(handoffData, null, 2));

    console.log(`   HANDOFF.json: ${handoffPath}`);
  }

  async commitChanges() {
    console.log('💾 Committing changes...');

    // Check if there are changes to commit
    const status = spawnSync('git', ['status', '--porcelain'], {
      encoding: 'utf8',
      cwd: this.projectRoot
    });

    if (!status.stdout?.trim()) {
      console.log('   No changes to commit');
      return;
    }

    // Add all changes
    spawnSync('git', ['add', '-A'], {
      cwd: this.projectRoot,
      stdio: 'ignore'
    });

    // Commit
    const commitResult = spawnSync('git', ['commit', '-m', `[gsd-pause] ${this.changeId} - Session ${this.sessionId}`], {
      encoding: 'utf8',
      cwd: this.projectRoot
    });

    if (commitResult.status === 0) {
      console.log('   Changes committed');
    } else {
      console.log('   Commit skipped (possibly nothing to commit)');
    }
  }
}

// CLI interface
function main() {
  const args = process.argv.slice(2);
  const changeId = args[0] || 'default-change';

  const pauser = new GsdPauseWork(changeId);
  
  pauser.execute().then(result => {
    process.exit(result.success ? 0 : 1);
  }).catch(err => {
    console.error('Fatal error:', err);
    process.exit(1);
  });
}

if (import.meta.url === `file://${process.argv[1]}`) {
  main();
}

export { GsdPauseWork };
